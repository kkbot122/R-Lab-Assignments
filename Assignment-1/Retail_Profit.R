# Q1. Retail Store Profit Analysis
# creating objects
quantity_sold <- 850
selling_price <- 1250
cost_price <- 950

# calculate results
total_revenue <- selling_price * quantity_sold
total_cost <- cost_price * quantity_sold
total_profit <- total_revenue - total_cost

# print results
cat("Total Revenue:", total_revenue, "\n")
cat("Total Cost:", total_cost, "\n")
cat("Total profit:", total_profit, "\n")

